#include "../includes/fractol.h"

int		move_julia(int x, int y, t_p *p)
{
	if (p->space == 0 && x >= 0 && x <= WIDTH_W && y >= 0 && y <= HEIGHT_W)
	{
		p->mx = x;
		p->my = y;
		put_fractale(p);
	}
	return (0);
}
